package io.nuite.modules.system.erp.entity;

import java.io.Serializable;

public class KeHu implements Serializable {
    private static final long serialVersionUID = 1L;
    /**
     * 厂家标志
     */
    private String companyFalg;
    /**
     * 账号
     */
    private String accountName;

    /**
     * 姓名
     */
    private String userName;
    /**
     * 电话
     */
    private String telephone;
    /**
     * 电话
     */
    private String tel;
    private String address;
    /**
     * 联系人
     */
    private String linkman;
    /**
     * 性质代码
     * ‘0’－代理客户
     * ‘1’－加盟店（备用）
     * ‘2’－直营店
     * ‘3’－分公司
     * ‘4’－物料客户
     */
    private String userType;

    private String quyuCode;

    private String quyuName;

    private String qudaoCode;

    private String qudaoName;

    /**
     * 账号所属类型：1.工厂，2.分公司，3.代理商，4.工厂子账号
     */
    private Integer attachType;

    /**
     * 账号销售类型：1.工厂，2.贴牌商，3.批发商，4.直营店
     */
    private Integer saleType;

    public String getAccountName() {
        return accountName;
    }

    public void setAccountName(String accountName) {
        this.accountName = accountName;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getLinkman() {
        return linkman;
    }

    public void setLinkman(String linkman) {
        this.linkman = linkman;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getCompanyFalg() {
        return companyFalg;
    }

    public void setCompanyFalg(String companyFalg) {
        this.companyFalg = companyFalg;
    }

    public String getUserType() {
        return userType;
    }

    public void setUserType(String userType) {
        this.userType = userType;
    }

    public String getQuyuCode() {
        return quyuCode;
    }

    public void setQuyuCode(String quyuCode) {
        this.quyuCode = quyuCode;
    }

    public String getQuyuName() {
        return quyuName;
    }

    public void setQuyuName(String quyuName) {
        this.quyuName = quyuName;
    }

    public String getQudaoCode() {
        return qudaoCode;
    }

    public void setQudaoCode(String qudaoCode) {
        this.qudaoCode = qudaoCode;
    }

    public String getQudaoName() {
        return qudaoName;
    }

    public void setQudaoName(String qudaoName) {
        this.qudaoName = qudaoName;
    }

    public Integer getAttachType() {
        return attachType;
    }

    public void setAttachType(Integer attachType) {
        this.attachType = attachType;
    }

    public Integer getSaleType() {
        return saleType;
    }

    public void setSaleType(Integer saleType) {
        this.saleType = saleType;
    }
}
