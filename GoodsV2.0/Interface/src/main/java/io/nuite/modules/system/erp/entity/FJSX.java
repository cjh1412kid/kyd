package io.nuite.modules.system.erp.entity;

import java.io.Serializable;

/**
 * @Author: yangchuang
 * @Date: 2018/8/6 15:11
 * @Version: 1.0
 * @Description:
 */



public class FJSX implements Serializable {
    private static final long serialVersionUID = 1L;

    /**
     * 序号(主键)
     */
    private String seq;

    /**
     * 分类名称
     */
    private String name;

    public String getSeq() {
        return seq;
    }

    public void setSeq(String seq) {
        this.seq = seq;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        final StringBuilder sb = new StringBuilder("FJSX{");
        sb.append("seq='").append(seq).append('\'');
        sb.append(", name='").append(name).append('\'');
        sb.append('}');
        return sb.toString();
    }
}
