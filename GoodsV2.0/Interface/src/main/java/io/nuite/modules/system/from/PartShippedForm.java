package io.nuite.modules.system.from;

import org.springframework.web.multipart.MultipartFile;

import java.util.List;

/**
 * 部分发货表单
 */
public class PartShippedForm {
    private Integer orderSeq;
    private Integer expressCompanySeq;
    private String expressNo;
    private MultipartFile expressFile;
    private List<ShippedProductForm> list;

    public Integer getOrderSeq() {
        return orderSeq;
    }

    public void setOrderSeq(Integer orderSeq) {
        this.orderSeq = orderSeq;
    }

    public Integer getExpressCompanySeq() {
        return expressCompanySeq;
    }

    public void setExpressCompanySeq(Integer expressCompanySeq) {
        this.expressCompanySeq = expressCompanySeq;
    }

    public String getExpressNo() {
        return expressNo;
    }

    public void setExpressNo(String expressNo) {
        this.expressNo = expressNo;
    }

    public MultipartFile getExpressFile() {
        return expressFile;
    }

    public void setExpressFile(MultipartFile expressFile) {
        this.expressFile = expressFile;
    }

    public List<ShippedProductForm> getList() {
        return list;
    }

    public void setList(List<ShippedProductForm> list) {
        this.list = list;
    }
}
